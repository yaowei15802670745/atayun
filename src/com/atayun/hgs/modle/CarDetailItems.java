	public void setCariLoad(String cariLoad) {
		this.cariLoad = cariLoad;
	}

	public String getCariLength() {
		return cariLength;
	}

	public void setCariLength(String cariLength) {
		this.cariLength = cariLength;
	}

	public String getCariVolume() {
		return cariVolume;
	}

	public void setCariVolume(String cariVolume) {
		this.cariVolume = cariVolume;
	}

